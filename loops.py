#while loop:
i = 1
while i <= 10:
    print(i)
    i += 1 #This line is the update statement of the loop; Without this update the loop will turn into a infinite loop.
#printing the star patterns:
j = 1
while j <= 5:
    print(j * "*")
    j += 1 
k = 5 
while k >= 0:
    print(k * '*')
    k -= 1
for item in range(5):
    print(item)
