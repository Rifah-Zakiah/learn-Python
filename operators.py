#arithmatic operators: +, -, *, /
print(5+2)
print(6-3)
print(9*3)
print(8/4)
print(5/2)
print(5//2) #Prints only the integer value
print(5%2) #remainder
print(5**2) #pwer operator = 5 to the power 2
#shortcut operators:
i = 5
i += 2 # i = i+2
i -= 2 # i = i-2
i *= 2 # i = i*2
i /= 2 # i = i/2
#operator precedence:()>*>+
print(2+3*5) #=2+15=17
print((2+3)*5) #=5*5=25
#This is a comment in python.
#Comparison operator: ==, <, >, <=, >=, !=: returns value in boolean form(True or False)
print(3>2)#True
print(3<2)#False
print(3>=2)#True
print(3<=2)#False
print(3==2)#False
print(3!=2)#True
#Logical operators: and, or, not:(True or False)
print(3>2 or 2>1) #True if one condition is true.
print(3>4 and 9<22) #True if both conditions are true.
print(not 2>3) #Reverses the condition of the output.
